    private void btnAdicionarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarProdutoActionPerformed
        // TODO add your handling code here:
        String nome = textNome.getName();
        String unidade = (String)comboUnidade.getSelectedItem();
        double preco = (double)spinnerPreco.getValue();
        boolean nacional = radioProdutoNacional.isSelected();
        
        //criando o objeto
        Produto produto = new classes.Produto(nome, unidade, preco, nacional);
        
        //add na lista
        lista.add(produto);
    }//GEN-LAST:event_btnAdicionarProdutoActionPerformed

