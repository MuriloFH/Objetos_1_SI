    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        String nomePessoa = textFieldNomePessoa.getText().equals(true) ? textFieldNomePessoa.getText() : "NaN";
        String endereco = textFieldEnderecoPessoa.getText().equals(true) ? textFieldEnderecoPessoa.getText() : "NaN";
        String telefone = textFieldTelefone.getText().equals(true) ? textFieldTelefone.getText() : "NaN";
        String contaBancaria = textFieldContaBancaria.getText().equals(true) ? textFieldContaBancaria.getText() : "NaN";

        String[] dados = new String[4];
        dados[0] = nomePessoa;
        dados[1] = endereco;
        dados[2] = telefone;
        dados[3] = contaBancaria;

        for (int i = 0; i < dados.length; i++) {
            textArea.append(dados[i] + "\n");
        }
        textArea.append("\n");
        limpaCampos();
    }//GEN-LAST:event_btnCadastrarActionPerformed

